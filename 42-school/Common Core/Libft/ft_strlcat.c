#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	c;
	size_t	d;

	if (size <= ft_strlen(dest))
		return (size + ft_strlen(src));
	c = ft_strlen(dest);
	d = 0;
	while (src[d] && c < size - 1)
		dest[c++] = src[d++];
	dest[c] = '\0';
	return (ft_strlen(dest) + ft_strlen(&src[d]));
}
