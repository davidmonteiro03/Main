#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	i;

	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	i = -1;
	while (++i < ft_strlen(s))
		str[i] = (*f)(i, (char)s[i]);
	str[i] = '\0';
	return (str);
}
