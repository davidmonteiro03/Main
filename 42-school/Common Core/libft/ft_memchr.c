#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;

	p = (unsigned char *)s;
	i = -1;
	while (++i < n)
		if (p[i] == (unsigned char)c)
			return (&((unsigned char *)s)[i]);
	return (NULL);
}
