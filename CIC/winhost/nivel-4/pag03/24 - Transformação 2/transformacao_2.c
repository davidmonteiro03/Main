#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;
	int	list[100];

	scanf("%d", &n);
	i = 0;
	while (i < n)
	{
		scanf("%d", &list[i]);
		i++;
	}
	i = 0;
	while (i < n)
	{
		printf("%d\n", list[i] * (i + 1));
		i++;
	}
	return (0);
}
