#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;

	scanf("%d", &n);
	i = 1;
	while (i <= n)
	{
		if (n % i == 0)
			printf("%d\n", i);
		i++;
	}
	return (0);
}
