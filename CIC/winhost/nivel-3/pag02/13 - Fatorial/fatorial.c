#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;
	int	fat;

	scanf("%d", &n);
	i = 1;
	fat = 1;
	while (i <= n)
	{
		fat *= i;
		i++;
	}
	printf("%d", fat);
	return (0);
}
