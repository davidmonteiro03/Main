#include <stdio.h>

int	main(void)
{
	int	n;
	int	list[100];
	int	i;

	scanf("%d", &n);
	i = 0;
	while (i < n)
	{
		scanf("%d", &list[i]);
		i++;
	}
	i = 0;
	while (i < n)
	{
		if (list[i] % 3 == 0)
			printf("%d\n", list[i]);
		i++;
	}
	return (0);
}
