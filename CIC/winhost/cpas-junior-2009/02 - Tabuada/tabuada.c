#include <stdio.h>

int	main(void)
{
	int	i;
	int	a;
	int	n;

	scanf("%d", &a);
	i = 0;
	while (++i <= 10)
	{
		n = (int)a * i;
		printf("%dx%d=%d\n", a, i, n);
	}
	return (0);
}
