#include <stdio.h>

int	main(void)
{
	int	i;
	int	k;
	int	n;

	scanf("%d", &n);
	i = 0;
	while (++i <= n)
	{
		k = 0;
		while (++k <= n)
		{
			if (i == k || ((i == n && k == 1) || (k == n && i == 1)) || \
				((k > 1 && i > 1) && i == n - k + 1))
				printf(":");
			else
				printf("+");
		}
		printf("\n");
	}
	return (0);
}
