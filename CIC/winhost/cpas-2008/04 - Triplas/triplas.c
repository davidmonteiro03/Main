#include <stdio.h>

int	main(void)
{
	int	a[100];
	int	n;
	int	t;
	int	i;

	scanf("%d", &n);
	t = 0;
	i = -1;
	while (++i < n)
		scanf("%d", &a[i]);
	i = -1;
	while (++i < n)
		if (a[i] == a[i + 1] && a[i] == a[i - 1])
			t++;
	printf("%d", t);
	return (0);
}
