#include <stdio.h>

int	main(void)
{
	int	a;
	int	p;
	int	s;
	int	t;
	int	q;
	int	n;
	int	num;

	scanf("%d", &a);
	p = a / 100;
	s = (a / 10) % 10;
	t = (a % 100) % 10;
	n = a * 10;
	q = (p + s * 3 + t * 5) % 7;
	num = n + q;
	printf("%d", num);
	return (0);
}
