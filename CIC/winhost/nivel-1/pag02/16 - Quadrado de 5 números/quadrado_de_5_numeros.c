#include <stdio.h>

int	main(void)
{
	int	list[5];
	int	i;

	i = 0;
	while (i < 5)
	{
		scanf("%d", &list[i]);
		i++;
	}
	i = 0;
	while (i < 5)
	{
		printf("%d\n", list[i] * list[i]);
		i++;
	}
	return (0);
}
