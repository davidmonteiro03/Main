#include <stdio.h>
#include <math.h>

int	main(void)
{
	int	a;
	int	b;
	int	t;

	a = 3;
	b = 4;
	t = 1;
	while (t < 15)
	{
		a += 3;
		b += 4;
		t++;
	}
	printf("%d", (int)(sqrt(a * a + b * b)));
	return (0);
}
