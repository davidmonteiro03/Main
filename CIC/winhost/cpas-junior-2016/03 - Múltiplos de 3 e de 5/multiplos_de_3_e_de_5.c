#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;

	scanf("%d",&n);
	i = 0;
	while (++i < n)
	{
		if (i % 3 == 0 || i % 5 == 0)
			printf("%d\n", i);
	}
	return (0);
}
