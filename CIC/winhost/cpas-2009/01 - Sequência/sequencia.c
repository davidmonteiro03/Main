#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;

	scanf("%d", &n);
	i = 0;
	while (n != 1)
	{
		if (n % 2 == 0)
		{
			n = n / 2;
			printf("%d\n", n);
		}
		else if (n % 2 != 0)
		{
			n = n * 3 + 1;
			printf("%d\n", n);
		}
		i += 1;
	}
	printf("#%d", i);
	return (0);
}
