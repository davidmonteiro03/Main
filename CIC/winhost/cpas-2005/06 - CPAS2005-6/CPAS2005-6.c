#include <stdio.h>

int	main(void)
{
	int	x;
	int	y;
	int	t;
	int	i;

	i = 999;
	while (++i <= 9999)
	{
		x = i % 100;
		y = i / 100;
		t = (x + y) * (x + y);
		if (t == i)
			printf("%d\n", t);
	}
	return (0);
}
